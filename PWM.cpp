#include "PWM.h"
#include <cstdint>
#include <Arduino.h>

constexpr int FREQ = 20000;
constexpr uint8_t RESOLUTION = 8;

PWM::PWM(uint8_t motorPin)
  : m_motorPin(motorPin)
{
  ledcAttach(m_motorPin, FREQ, RESOLUTION);
}


void PWM::write(uint8_t value)
{
  ledcWrite(m_motorPin, value);
}
